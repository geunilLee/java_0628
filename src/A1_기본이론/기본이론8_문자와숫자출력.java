package A1_기본이론;

public class 기본이론8_문자와숫자출력 {

	public static void main(String[] args) {
		System.out.println(10 + 3);
		System.out.println("더하기 : " + 10 + 3);
		
		// +연산자는 같은 자료형끼리만 가능하다.
		System.out.println(10 + 3);
		System.out.println(10.3 + 3.3);
		System.out.println("김 + 철수");
		System.out.println("--------------------");
		// 정수 + 실수 ==> 정수가 실수로 바뀐다
		System.out.println(10 + 3.0);
		// 정수 + 문자 ==>
		

	}

}
