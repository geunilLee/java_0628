package A3_나누기_알고리즘;

public class 나누기_알고리즘_사과배_문제 {
	public static void main(String[] args) {	
		/*
			 [문제]
			 	- 사과 8개의 무게는 1056g 입니다.
			 	- 배는 사과의 117% 의 무게를 갖고있습니다.
			 	- 사과 5개 배7개를 구입하였습니다. 
			 	
			 	- 과일전체의 총무게를 구하시오.
		*/	
		
		/*
		 * [단주]
		 * 사과의 무게 (1056/8)
		 * 배의무게((1056/8)*1.17)
		 * 사과 5개 ((1056/8)*5)
		 * 배 7개 ((1056/8)*5)*7)
		 * 
		 */
		
		System.out.println(((1056/8.0)*5)+(1056/8.0*1.17*7));
     }
}